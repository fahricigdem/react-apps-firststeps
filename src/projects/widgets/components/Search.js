import React, { useState, useEffect } from 'react';
import axios from 'axios';

const Search = () => {
  const [term, setTerm] = useState('programming');
  const [results, setResults] = useState([]);
  const [timeoutId, setTimeoutId] = useState(0);//  --->i use this variable in place of clean up function blow in useEffect...

  useEffect(() => {
    const search = async () => {
      const { data } = await axios.get('https://en.wikipedia.org/w/api.php', {
        params: {
          action: 'query',
          list: 'search',
          origin: '*',
          format: 'json',
          srsearch: term,
        },
      });
      setResults(data.query.search);
    };

    if (term && !results.length) {
      search();
    } else {
      clearTimeout(timeoutId)  //  --->i use it in place of clean up function blow ...
      const timeoutID = setTimeout(() => {
        if (term) {
          search();
        }
      }, 1000);
      setTimeoutId(timeoutID)//  --->i use it in place of clean up function blow ... this gives info for clearTimeout() func above.
      // cleanup function : i can also use this function. useEffect will conduct this returned function next time of update.
      // return () => {
      // clearTimeout(timeoutId);
      // };
    }
  }, [term]);  //useEffect

  const renderedResults = results.map((result) => {
    return (
      <div key={result.pageid} className="item">
        <div className="right floated content">
          <a
            className="ui button"
            href={`https://en.wikipedia.org?curid=${result.pageid}`}
          >
            Go
          </a>
        </div>
        <div className="content">
          <div className="header">{result.title}</div>
          <span dangerouslySetInnerHTML={{ __html: result.snippet }}></span>
        </div>
      </div>
    );
  });

  return (
    <div>
      <div className="ui form">
        <div className="field">
          <label>Enter Search Term</label>
          <input
            value={term}
            onChange={(e) => setTerm(e.target.value)}
            className="input"
          />
        </div>
      </div>
      <div className="ui celled list">{renderedResults}</div>
    </div>
  );
};

export default Search;
